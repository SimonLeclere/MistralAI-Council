type PixelGlyph = readonly [
  string,
  string,
  string,
  string,
  string,
  string,
  string
];

type PixelFontBitmap = Record<string, PixelGlyph>;

type PixelFontCompiled = Record<string, number[]>;

const compileFont = (bitmap: PixelFontBitmap): PixelFontCompiled => {
  const compiled: PixelFontCompiled = {};

  for (const [char, rows] of Object.entries(bitmap)) {
    compiled[char] = rows.map((row) => {
      if (row.length !== 5) {
        throw new Error(`Invalid pixel-font row width for '${char}': '${row}'`);
      }
      return parseInt(row, 2);
    });
  }

  return compiled;
};

